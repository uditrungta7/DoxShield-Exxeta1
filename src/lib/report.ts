// ─── Helpers ──────────────────────────────────────────────────────────────────

function riskColor(level: string): string {
  switch (level?.toUpperCase()) {
    case 'LOW':    return '#10B981'
    case 'MEDIUM': return '#F59E0B'
    case 'HIGH':   return '#EF4444'
    case 'SEVERE': return '#7C3AED'
    default:       return '#9CA3AF'
  }
}

function riskBg(level: string): string {
  switch (level?.toUpperCase()) {
    case 'LOW':    return '#10B98122'
    case 'MEDIUM': return '#F59E0B22'
    case 'HIGH':   return '#EF444422'
    case 'SEVERE': return '#7C3AED22'
    default:       return '#9CA3AF22'
  }
}

function jurisdictionFlag(j: string): string {
  const flags: Record<string, string> = {
    US: '🇺🇸', EU: '🇪🇺', GB: '🇬🇧', CN: '🇨🇳', RU: '🇷🇺',
    DE: '🇩🇪', FR: '🇫🇷', NL: '🇳🇱', IE: '🇮🇪', SE: '🇸🇪',
    CH: '🇨🇭', CA: '🇨🇦', AU: '🇦🇺', JP: '🇯🇵', KR: '🇰🇷',
    SG: '🇸🇬', IN: '🇮🇳', BR: '🇧🇷', Unknown: '🌐',
  }
  return flags[j] || flags[j?.toUpperCase()] || '🌐'
}

function fmt(date: Date): string {
  return date.toLocaleDateString('en-GB', {
    day: '2-digit', month: 'long', year: 'numeric',
    hour: '2-digit', minute: '2-digit',
  })
}

function esc(s: any): string {
  if (s == null) return '—'
  return String(s)
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;')
}

function scoreToColor(score: number): string {
  if (score >= 80) return '#10B981'
  if (score >= 60) return '#F59E0B'
  if (score >= 40) return '#EF4444'
  return '#7C3AED'
}

