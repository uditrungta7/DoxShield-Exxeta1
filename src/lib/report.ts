// ─── Public export ────────────────────────────────────────────────────────────

export async function exportReport(store: StoreState): Promise<void> {
  // 1. Fetch fresh network + cookie data
  const [connRes, cookieRes] = await Promise.allSettled([
    fetch(`${SIDECAR_BASE}/api/network/live`).then(r => r.json()),
    fetch(`${SIDECAR_BASE}/api/cookies`).then(r => r.json()),
  ])

  const connections =
    connRes.status === 'fulfilled'
      ? Array.isArray(connRes.value) ? connRes.value : []
      : store.connections || []

  const cookieData = cookieRes.status === 'fulfilled' ? cookieRes.value : {}
  const cookies = cookieData.cookies ?? store.cookies ?? []
  const cookieStats = cookieData.stats ?? {}

  const data = {
    user: store.user,
    overall: store.overallRisk,
    apps: store.apps || [],
    connections,
    cookies,
    cookieStats,
    lastScanAt: store.lastScanAt,
    generatedAt: new Date(),
  }

  const html = buildHTML(data)
  const filename = `Doxshield-Report-${data.generatedAt.toISOString().split('T')[0]}.pdf`

  const savedPath = await window.sovereign?.savePdf(html, filename)
  if (savedPath) {
    window.sovereign?.openExternal(`file://${savedPath}`)
  }
}
