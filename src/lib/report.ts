function buildRecommendations(data: any): string {
  const { apps, cookies, connections, overall } = data
  const score = overall?.sovereignty_score ?? overall?.score ?? 0

  interface Recommendation {
    urgency: 'URGENT' | 'SHORT-TERM' | 'ONGOING'
    text: string
  }

  const recs: Recommendation[] = []

  // CN cookies
  const cnCookies = (cookies || []).filter((c: any) => c.jurisdiction?.toUpperCase() === 'CN')
  const deepseekCookies = cnCookies.filter((c: any) => c.domain?.includes('deepseek') || c.domain?.includes('tiktok') || c.domain?.includes('bytedance'))
  if (deepseekCookies.length > 0) {
    recs.push({ urgency: 'URGENT', text: 'Remove DeepSeek/TikTok cookies — Chinese law requires data sharing with the government. Clear cookies in your browser settings and consider blocking these domains.' })
  } else if (cnCookies.length > 0) {
    recs.push({ urgency: 'URGENT', text: `Remove ${cnCookies.length} Chinese-jurisdiction cookie${cnCookies.length > 1 ? 's' : ''} — companies operating under Chinese law must share user data with authorities upon request.` })
  }

  // RU cookies
  const ruCookies = (cookies || []).filter((c: any) => c.jurisdiction?.toUpperCase() === 'RU')
  if (ruCookies.length > 0) {
    recs.push({ urgency: 'URGENT', text: `Remove ${ruCookies.length} Russian-jurisdiction cookie${ruCookies.length > 1 ? 's' : ''} — Russian law requires local data storage and access for government agencies.` })
  }

  // Facebook/Meta
  const fbCookies = (cookies || []).filter((c: any) => c.domain?.includes('facebook') || c.domain?.includes('instagram') || c.domain?.includes('meta'))
  if (fbCookies.length > 0) {
    recs.push({ urgency: 'SHORT-TERM', text: 'Consider blocking Facebook/Meta trackers using a browser extension like uBlock Origin or Privacy Badger to reduce cross-site tracking.' })
  }

  // SEVERE/HIGH apps
  const severeApps = (apps || []).filter((a: any) => ['SEVERE', 'HIGH'].includes(a.risk_level?.toUpperCase())).slice(0, 4)
  severeApps.forEach((a: any) => {
    const name = a.app_name ?? a.name ?? 'Unknown App'
    const factor = a.top_risk_factor ?? a.risk_factor ?? 'data collection practices'
    recs.push({ urgency: a.risk_level?.toUpperCase() === 'SEVERE' ? 'URGENT' : 'SHORT-TERM', text: `Review ${name} — ${factor}. Consider whether this app is necessary or if a privacy-respecting alternative exists.` })
  })

  // VPN
  const vpnConn = (connections || []).find((c: any) =>
    c.vpn || c.is_vpn || c.app?.toLowerCase().includes('vpn') ||
    c.app?.toLowerCase().includes('wireguard') || c.app?.toLowerCase().includes('nordvpn')
  )
  if (vpnConn) {
    recs.push({ urgency: 'ONGOING', text: `Your VPN (${vpnConn.app || 'VPN Client'}) routes traffic through ${vpnConn.country || 'an unknown jurisdiction'} — verify this aligns with your privacy needs and that your VPN provider has a verified no-logs policy.` })
  }

  // Low sovereignty score
  if (score < 50) {
    recs.push({ urgency: 'SHORT-TERM', text: `Your sovereignty score of ${score}/100 is below the recommended threshold. Consider switching to EU-based software alternatives (e.g., Proton for email, Nextcloud for storage, DuckDuckGo for search).` })
  }

  // No FDA / Safari cookies
  const hasSafariCookies = (cookies || []).some((c: any) => c.browser?.toLowerCase().includes('safari') || c.source?.toLowerCase().includes('safari'))
  if (!hasSafariCookies) {
    recs.push({ urgency: 'ONGOING', text: 'Grant Full Disk Access to Doxshield in System Settings → Privacy & Security → Full Disk Access to enable complete Safari cookie scanning.' })
  }

  // Trackers
  const trackerCount = (cookies || []).filter((c: any) => c.known_tracker).length
  if (trackerCount > 10) {
    recs.push({ urgency: 'SHORT-TERM', text: `${trackerCount} known trackers detected. Install uBlock Origin (Firefox/Chrome) and enable the EasyPrivacy + Fanboy's Annoyance filter lists.` })
  }

  // Generic ongoing
  recs.push({ urgency: 'ONGOING', text: 'Run a full Doxshield scan weekly to monitor changes in your data sovereignty posture.' })
  recs.push({ urgency: 'ONGOING', text: 'Review your installed applications regularly and uninstall apps from high-risk jurisdictions that you no longer actively use.' })

  const urgent = recs.filter(r => r.urgency === 'URGENT')
  const shortTerm = recs.filter(r => r.urgency === 'SHORT-TERM')
  const ongoing = recs.filter(r => r.urgency === 'ONGOING')

  const urgencyColor: Record<string, string> = {
    URGENT: '#EF4444',
    'SHORT-TERM': '#F59E0B',
    ONGOING: '#3B82F6',
  }
  const urgencyBg: Record<string, string> = {
    URGENT: '#EF444422',
    'SHORT-TERM': '#F59E0B22',
    ONGOING: '#3B82F622',
  }

  function recGroup(items: Recommendation[], label: string) {
    if (items.length === 0) return ''
    const urgency = items[0].urgency
    return `
    <div class="rec-group">
      <div class="rec-group-header">
        <span class="rec-badge" style="background:${urgencyBg[urgency]};color:${urgencyColor[urgency]};border:1px solid ${urgencyColor[urgency]}44">${label}</span>
      </div>
      ${items.map(r => `
      <div class="rec-item" style="border-left:3px solid ${urgencyColor[r.urgency]}">
        <p>${esc(r.text)}</p>
      </div>`).join('')}
    </div>`
  }

  return `
  <!-- PAGE 6: RECOMMENDATIONS -->
  <div class="page last-page">
    <div class="page-header">
      <div class="page-header-left">
        <div class="section-eyebrow">Actionable Recommendations</div>
        <h1 class="section-title">Improving Your Data Sovereignty</h1>
      </div>
    </div>

    <p class="section-desc">
      Based on your scan results, the following actions are recommended to improve your data sovereignty score.
      Items are prioritised by urgency and potential privacy impact.
    </p>

    ${recGroup(urgent, 'URGENT — Action Required')}
    ${recGroup(shortTerm, 'SHORT-TERM — Within 30 days')}
    ${recGroup(ongoing, 'ONGOING — Regular Maintenance')}

    <!-- Final note -->
    <div class="final-note">
      <strong>About Doxshield</strong><br>
      Doxshield is a macOS data sovereignty monitor that helps you understand where your data goes and who controls it.
      All analysis is performed locally on your device — your scan results are never sent to external servers.
    </div>

    <div class="page-footer">
      Doxshield &middot; Data Sovereignty Report &middot; ${fmt(data.generatedAt)} &middot; Page <span class="pagenum"></span>
    </div>
  </div>`
}

